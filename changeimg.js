function cImg(self) {
  if(document.querySelector('body').className ==='Yes_HB') {
    document.querySelector('body').className = 'No_HB';
    document.body.style.backgroundImage = "url('nohorseballon.png')";
  }
  else {
    document.querySelector('body').className = 'Yes_HB';
    document.body.style.backgroundImage = "url('min_final.png')";
  }
}
